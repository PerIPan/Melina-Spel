// Translation functions
export function t(lang: Language, key: string, vars?: Record<string, string | number>): string {
  let text = ui[lang][key] || ui.en[key] || key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

export function tQuestion(lang: Language, englishQuestion: string): string {
  if (lang === "en") return englishQuestion;
  return questions[lang][englishQuestion] || englishQuestion;
}

export function tAnimal(lang: Language, englishAnimal: string): string {
  if (lang === "en") return englishAnimal;
  return animals[lang][englishAnimal] || englishAnimal;
}

export function getSpeechLang(lang: Language): string {
  return LANGUAGES.find((l) => l.code === lang)?.speechLang || "en-US";
}
